                    <h2 class="text-uppercase fw-semibold" data-aos="flip-down">Admission Process</h2>
                    <h5 class="fw-light" data-aos="fade-right">We welcome prospective students to our school community
                        and are excited to guide you through our comprehensive admission process. The process is
                        designed to ensure that each applicant is given the opportunity to demonstrate their potential,
                        and to assess whether our school is the right fit for their academic journey.</h5>
                    <h4 class="fst-italic fw-semibold mt-5" data-aos="fade-down">For Grades 7 through 12, the admission
                        process consists of the following steps:</h4>
                    <ol>
                        <li class="my-4 fw-semibold" data-aos="fade">
                            <div class="criteria">
                                <h5 class="m-0 fw-semibold" data-aos="fade-down">Academic Records</h5>
                                <h5 class="fw-light" data-aos="fade-right">Applicants are required to provide a
                                    photocopy of their current report card (Form 138) for verification purposes.
                                    Original documents must be presented during the submission. To be eligible for
                                    admission, a minimum final average of 85% from the previous high school level is
                                    required, with no individual grade falling below 80%.</h5>
                            </div>
                        </li>
                        <li class="my-4 fw-semibold" data-aos="fade">
                            <div class="criteria">
                                <h5 class="m-0 fw-semibold" data-aos="fade-down">Conduct Evaluation</h5>
                                <h5 class="fw-light" data-aos="fade-right">We believe that behavioral conduct is an
                                    important aspect of a student's overall development. Therefore, a satisfactory
                                    deportment grade is essential as part of the admission process. This grade helps us
                                    gauge the applicant's conduct and behavior in their previous academic environment.
                                </h5>
                            </div>
                        </li>
                        <li class="my-4 fw-semibold" data-aos="fade">
                            <div class="criteria">
                                <h5 class="m-0 fw-semibold" data-aos="fade-down">Recommendation Letters</h5>
                                <h5 class="fw-light" data-aos="fade-right">To better understand the applicant's
                                    character and academic potential, we require two letters of recommendation from the
                                    applicant's most recent school. These letters should ideally come from the Principal
                                    and the adviser or counselor who are familiar with the applicant's academic
                                    performance and behavior.
                                </h5>
                            </div>
                        </li>
                        <li class="my-4 fw-semibold" data-aos="fade">
                            <div class="criteria">
                                <h5 class="m-0 fw-semibold" data-aos="fade-down">Birth Certificate</h5>
                                <h5 class="fw-light" data-aos="fade-right">A verified copy of the applicant's birth
                                    certificate from the Local Civil Registrar or the Philippine Statistics Authority
                                    (PSA) is a mandatory submission. This document serves as official proof of the
                                    applicant's identity and age.</h5>
                            </div>
                        </li>
                    </ol>
                    <h5 class="mt-3 fw-light" data-aos="fade-right">As you embark on this journey to join our school
                        community, we encourage you to carefully prepare and submit all required documents. The
                        admission process is a holistic evaluation that takes into account academic performance,
                        conduct, character references, and identity verification. Our aim is to create an inclusive and
                        thriving learning environment where each student's potential is recognized and nurtured.</h5>
                    <h5 class="mt-3 fw-light" data-aos="fade-right">We look forward to getting to know you better
                        through this process and welcoming you to our school. If you have any questions or need
                        assistance with any step of the admission process, please feel free to reach out to our
                        Admissions Office. We are here to support you every step of the way.</h5>
                    <h5 class="mt-3 fw-light" data-aos="fade-right">In addition to meeting these requirements,
                        student/pupil applicants must successfully complete the Qualifying Entrance Test and may also be
                        subject to an interview (in specific cases).</h5>
                    <h5 class="mt-3 fw-light" data-aos="fade-right">Applicants who fall short of meeting the
                        admission criteria have the option to submit an appeal to the Board of Admission for
                        reconsideration. The Board has the authority to review all cases related to admission and
                        re-admission.</h5>
                    <h5 class="mt-3 fw-light" data-aos="fade-right">We look forward to welcoming students who
                        demonstrate potential and a commitment to academic excellence into our Junior/Senior High School
                        Departments.</h5>
