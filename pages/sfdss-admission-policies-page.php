                        Probation</h2>
                    <h4 class="fst-italic fw-semibold mt-4" data-aos="fade-down">Academic Probation Status</h4>
                    <h5 class="mt-4 fw-light" data-aos="fade-right">Academic Probation Status serves as an initial
                        formal warning, conveying
                        the
                        condition that a
                        marked improvement in academic performance is imperative for re-admission and/or promotion.</h5>
                    <h5 class="mt-4 fw-light" data-aos="fade-right">This status is pertinent to the subsequent
                        scenarios:</h5>
                    <ol type="a" class="fw-semibold">
                        <li class="my-4" data-aos="fade">
                            <h5 class="fw-semibold" data-aos="fade-down">Student-applicants who did not meet admission
                                prerequisites</h5>
                            <h5 class="fw-light" data-aos="fade-right">Should a student not meet the requirements for
                                admission or fail in any
                                subject by the
                                conclusion of the academic year, re-admission might not be assured.</h5>
                        </li>
                        <li class="my-4" data-aos="fade">
                            <h5 class="fw-semibold" data-aos="fade-down">Students with a single failing grade in a
                                subject after a quarter
                            </h5>
                            <h5 class="fw-light" data-aos="fade-right">Any student acquiring a failing grade in a
                                subject following a quarter
                                may be placed
                                under probation.</h5>
                        </li>
                    </ol>
                    <h4 class="fst-italic fw-semibold mt-4" data-aos="fade-down">Strict Probation Status</h4>
                    <h5 class="mt-4 fw-light" data-aos="fade-right">Strict Probation Status represents a more solemn
                        caution, stipulating that
                        significant enhancement in academic accomplishments is obligatory for re-admission and/or
                        promotion. It is applicable to students who obtain a failing grade in one laboratory subject or
                        two non-laboratory subjects at the culmination of a quarter.</h5>
                    <h4 class="fst-italic fw-semibold mt-4" data-aos="fade-down">Very Strict Probation</h4>
                    <h5 class="mt-4 fw-light" data-aos="fade-right">Very Strict Probation is the ultimate admonition,
                        entailing specific
                        prerequisites
                        for re-admission and/or promotion. It applies under the ensuing circumstances:</h5>
                    <ol type="a" class="fw-semibold">
                        <li class="my-4" data-aos="fade">
                            <h5 class="fw-semibold" data-aos="fade-down">Failing grades in multiple subjects</h5>
                            <h5 class="fw-light" data-aos="fade-right">Should a student receive failing grades in one
                                laboratory subject and
                                one or more
                                non-laboratory subjects, or in three or more non-laboratory subjects by the culmination
                                of the academic year, re-admission for the subsequent school year might be precluded.
                            </h5>
                        </li>
                        <li class="my-4" data-aos="fade">
                            <h5 class="fw-semibold" data-aos="fade-down">Repeating a year level or being denied
                                admission</h5>
                            <h5 class="fw-light" data-aos="fade-right">A student encountering failing grades in one
                                laboratory subject or one
                                or two
                                non-laboratory subjects at the close of the academic year could lead to grade repetition
                                or non-admittance.
                            </h5>
                        </li>
                    </ol>
                    <h5 class="mt-4 fw-light" data-aos="fade-right"><span class="fst-italic fw-semibold">Note:</span>
                        The status of a student
                        in
                        Academic Probation is annulled upon passing all subjects
                        for a given quarter, thereby reversing any prior probationary standing they held.</h5>
