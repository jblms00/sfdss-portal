                                    need to submit a photocopy of your current report card (Form 138) for verification,
                                    along with the original documents. Additionally, provide two recommendation letters,
                                    your birth certificate, and any other required materials.</h5>
                            </div>
                        </li>
                        <li class="my-4 fw-semibold" data-aos="fade">
                            <div class="description">
                                <h5 class="m-0 fw-semibold" data-aos="fade-down">Is the Qualifying Entrance Test
                                    mandatory for all applicants?</h5>
                                <h5 class="fw-light" data-aos="fade-right">Yes, the Qualifying Entrance Test is
                                    mandatory for all student/pupil applicants. It helps assess your readiness for our
                                    educational environment.</h5>
                            </div>
                        </li>
                        <li class="my-4 fw-semibold" data-aos="fade">
                            <div class="description">
                                <h5 class="m-0 fw-semibold" data-aos="fade-down">What happens if I don't meet the
                                    minimum academic requirements?</h5>
                                <h5 class="fw-light" data-aos="fade-right">If you don't meet the minimum academic
                                    requirements, you have the option to submit an appeal to the Board of Admission for
                                    reconsideration. The Board reviews each case individually.</h5>
                            </div>
                        </li>
                        <li class="my-4 fw-semibold" data-aos="fade">
                            <div class="description">
                                <h5 class="m-0 fw-semibold" data-aos="fade-down">Can I be admitted on Academic
                                    Probation?</h5>
                                <h5 class="fw-light" data-aos="fade-right">Yes, students who don't fully meet the
                                    academic prerequisites can be admitted on Academic Probation. This offers an
                                    opportunity to improve your academic performance.</h5>
                            </div>
                        </li>
                        <li class="my-4 fw-semibold" data-aos="fade">
                            <div class="description">
                                <h5 class="m-0 fw-semibold" data-aos="fade-down">What are the different levels of
                                    Academic Probation?</h5>
                                <h5 class="fw-light" data-aos="fade-right">There are three levels of Academic Probation:
                                </h5>
                                <ol type="1">
                                    <li class="my-4 fw-semibold" data-aos="fade-right">
                                        <h5 class="fw-light m-0"><span class="fst-italic fw-semibold">Academic Probation
                                                Status:
                                            </span>
                                            An initial formal
                                            warning to improve
                                            academic
                                            performance for re-admission/promotion.</h5>
                                    </li>
                                    <li class="my-4 fw-semibold" data-aos="fade-right">
                                        <h5 class="fw-light m-0"><span class="fst-italic fw-semibold">Strict Probation
                                                Status:</span>
                                            A
                                            more serious
                                            caution, requiring
                                            significant enhancement in academic accomplishments.</h5>
                                    </li>
                                    <li class="my-4 fw-semibold" data-aos="fade-right">
                                        <h5 class="fw-light m-0"><span class="fst-italic fw-semibold">Very Strict
                                                Probation:</span>
                                            The
                                            highest level of
                                            caution, with
                                            specific prerequisites for re-admission/promotion.</h5>
                                    </li>
                                </ol>
                            </div>
                        </li>
                        <li class="my-4 fw-semibold" data-aos="fade">
                            <div class="description">
                                <h5 class="m-0 fw-semibold" data-aos="fade-down">What happens if I continue to struggle
                                    academically during my probationary period?</h5>
                                <h5 class="fw-light" data-aos="fade-right">Depending on the severity, struggling
                                    academically during your probationary period might affect your re-admission or
                                    promotion. Communication with the Board of Admission is crucial in such cases.</h5>
                            </div>
                        </li>
                        <li class="my-4 fw-semibold" data-aos="fade">
                            <div class="description">
                                <h5 class="m-0 fw-semibold" data-aos="fade-down">Can my Academic Probation status be
                                    lifted?</h5>
                                <h5 class="fw-light" data-aos="fade-right">Yes, if you pass all subjects for a specific
                                    quarter, your Academic Probation status can be canceled. This reflects your
                                    commitment to improving your academic performance.</h5>
                            </div>
                        </li>
                        <li class="my-4 fw-semibold" data-aos="fade">
                            <div class="description">
                                <h5 class="m-0 fw-semibold" data-aos="fade-down">How do recommendation letters
                                    contribute to my application?</h5>
                                <h5 class="fw-light" data-aos="fade-right">Recommendation letters provide insights into
                                    your character and abilities from your previous school. Ideally, these letters
                                    should come from your Principal and adviser/counselor.</h5>
                            </div>
                        </li>
                        <li class="my-4 fw-semibold" data-aos="fade">
                            <div class="description">
                                <h5 class="m-0 fw-semibold" data-aos="fade-down">What happens if I excel academically in
                                    one quarter but not in others?</h5>
                                <h5 class="fw-light" data-aos="fade-right">Excelling academically in one quarter is
                                    positive progress, but it's essential to maintain consistent improvement to address
                                    previous academic concerns.</h5>
                            </div>
                        </li>
                        <li class="my-4 fw-semibold" data-aos="fade">
                            <div class="description">
                                <h5 class="m-0 fw-semibold" data-aos="fade-down">How do I appeal my admission decision
                                    if I'm not accepted?</h5>
                                <h5 class="fw-light" data-aos="fade-right">If your admission is not accepted, you can
                                    appeal to the Board of Admission for reconsideration. The Board reviews all cases
                                    related to admission and re-admission.</h5>
                            </div>
                        </li>
